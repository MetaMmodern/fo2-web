const fs = require("fs");
const path = require("path");

const repoRoot = path.resolve(__dirname, "..");
const srcRoot = path.join(repoRoot, "src");
const outputPath = path.join(srcRoot, "game/generated/runtimeAssetCatalog.js");
const tracksRoot = path.join(srcRoot, "data/tracks");
const carsRoot = path.join(srcRoot, "data/cars");
const globalRoot = path.join(srcRoot, "data/global");
const databaseRoot = path.join(
  srcRoot,
  "data/database/flatout2.db extracted/root",
);

const trackAliases = {
  colormap: "col",
};

const trackDbFolderMap = {
  arena: { family: "Arena", name: "Arena1" },
  city: { family: "City", name: "City1" },
  forest: { family: "Forest", name: "Forest1" },
  garagetest: { family: "GarageTest", name: "GarageTest1" },
};

const lines = [
  "// Generated by tools/generate-runtime-catalog.js",
  "import * as THREE from \"three\";",
  "",
];

let importIndex = 0;

function addImport(filePath, { url = true } = {}) {
  const importName = `asset${importIndex++}`;
  const relativePath = path
    .relative(path.dirname(outputPath), filePath)
    .replace(/\\/g, "/");
  const importPrefix = url ? "url:" : "";
  lines.push(`import ${importName} from "${importPrefix}${relativePath}";`);
  return importName;
}

function getDirEntries(dirPath) {
  return fs.readdirSync(dirPath, { withFileTypes: true });
}

function parseQuotedValue(text, key) {
  const match = text.match(new RegExp(`${key}\\s*=\\s*"([^"]+)"`));
  return match ? match[1] : null;
}

function parseNumber(text, key) {
  const match = text.match(new RegExp(`${key}\\s*=\\s*([-+]?\\d*\\.?\\d+)`));
  return match ? Number.parseFloat(match[1]) : null;
}

function parseVector(text, key, expectedLength) {
  const match = text.match(new RegExp(`${key}\\s*=\\s*\\{\\s*([^}]+)\\}`));

  if (!match) {
    return null;
  }

  const values = match[1]
    .split(",")
    .map((value) => Number.parseFloat(value.trim()))
    .filter((value) => Number.isFinite(value));

  return values.length === expectedLength ? values : null;
}

function fileExists(filePath) {
  return fs.existsSync(filePath);
}

function createTextureFileMap(dirPath) {
  return new Map(
    getDirEntries(dirPath)
      .filter((entry) => entry.isFile() && entry.name.toLowerCase().endsWith(".png"))
      .map((entry) => [
        path.parse(entry.name).name.toLowerCase(),
        path.join(dirPath, entry.name),
      ]),
  );
}

function normalizeTextureName(textureName) {
  if (!textureName) {
    return null;
  }

  const baseName = path.parse(textureName).name.toLowerCase();
  return trackAliases[baseName] || baseName;
}

function resolveSkyTopImport({
  weatherText,
}) {
  const skyDomeFile = parseQuotedValue(weatherText, "SkyDomeFile");
  const globalSkyTopPath = skyDomeFile
    ? path.join(globalRoot, "skybox", `${skyDomeFile}_u.png`)
    : null;

  if (globalSkyTopPath && fileExists(globalSkyTopPath)) {
    return addImport(globalSkyTopPath);
  }
  return "null";
}

function collectRequestedTrackTextures(logText) {
  return Array.from(
    new Set(
      logText
        .split(/\r?\n/)
        .filter((line) => /^Texture [123]:/.test(line))
        .map((line) => line.replace(/^Texture [123]:\s*/, "").trim())
        .filter(Boolean)
        .map(normalizeTextureName),
    ),
  ).sort((left, right) => left.localeCompare(right));
}

function formatVectorCtor(type, values) {
  if (!values) {
    return "null";
  }

  return `new THREE.${type}(${values.join(", ")})`;
}

