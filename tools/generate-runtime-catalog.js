function createCarDbIndex() {
  const index = new Map();
  const carDefsRoot = path.join(databaseRoot, "Data/Cars");
  const pendingDirs = [carDefsRoot];

  while (pendingDirs.length > 0) {
    const currentDir = pendingDirs.pop();

    for (const entry of getDirEntries(currentDir)) {
      const fullPath = path.join(currentDir, entry.name);

      if (entry.isDirectory()) {
        pendingDirs.push(fullPath);
        continue;
      }

      if (!entry.name.toLowerCase().endsWith(".h")) {
        continue;
      }

      const text = fs.readFileSync(fullPath, "utf8");
      const dataPath = parseQuotedValue(text, "DataPath");

      if (!dataPath) {
        continue;
      }

      const match = dataPath.match(/data\/Cars\/Car_(\d+)\/?/i);

      if (!match) {
        continue;
      }

      const carId = `car_${Number.parseInt(match[1], 10)}`;
      index.set(carId, { definitionPath: fullPath, text });
    }
  }

  return index;
}

function createGlobalImports() {
  const sharedTextureImports = {
    common: addImport(path.join(carsRoot, "shared/common.png")),
    interior: addImport(path.join(carsRoot, "shared/interior.png")),
    windows: addImport(path.join(carsRoot, "shared/windows.png")),
    lights: addImport(path.join(carsRoot, "shared/lights.png")),
    shock: addImport(path.join(carsRoot, "shared/shock.png")),
    shadow: addImport(path.join(carsRoot, "shared/shadow.png")),
  };

  const flareConfigByName = {};
  const flareGlowTextureByName = {};
  const flareTextureByName = {};

  for (const [fileName, key] of [
    ["day.ini", "day.ini"],
    ["canalday.ini", "canalday.ini"],
    ["sunevening.ini", "sunevening.ini"],
    ["moon.ini", "moon.ini"],
  ]) {
    flareConfigByName[key] = addImport(path.join(globalRoot, "flares", fileName));
  }

  for (const [fileName, key] of [
    ["day_glow.png", "day_glow.png"],
    ["canalday_glow.png", "canalday_glow.png"],
    ["evening_glow.png", "evening_glow.png"],
    ["moon_glow.png", "moon_glow.png"],
  ]) {
    flareGlowTextureByName[key] = addImport(path.join(globalRoot, "flares", fileName));
  }

  for (const [fileName, key] of [
    ["day_flares.png", "day_flares.png"],
    ["canalday_flares.png", "canalday_flares.png"],
    ["evening_flares.png", "evening_flares.png"],
    ["moon_flares.png", "moon_flares.png"],
  ]) {
    flareTextureByName[key] = addImport(path.join(globalRoot, "flares", fileName));
  }

  return {
    sharedTextureImports,
    flareConfigByName,
    flareGlowTextureByName,
    flareTextureByName,
  };
}

function buildCars(globalImports) {
  const dbIndex = createCarDbIndex();
  const carEntries = getDirEntries(carsRoot)
    .filter((entry) => entry.isDirectory() && /^car_\d+$/i.test(entry.name))
    .sort((left, right) => left.name.localeCompare(right.name, undefined, { numeric: true }));

  const carLiterals = carEntries.map((entry) => {
    const carId = entry.name.toLowerCase();
    const carDir = path.join(carsRoot, entry.name);
    const modelFileName = [
      "model_assimp.glb",
      "model.glb",
      "body_out.glb",
      "body.glb",
    ].find((fileName) => fileExists(path.join(carDir, fileName)));

    if (!modelFileName) {
      throw new Error(`No GLB model found for ${carId}`);
    }

    const modelImport = addImport(path.join(carDir, modelFileName));
    const cameraConfigImport = addImport(path.join(carDir, "camera.ini"));
    const bodyConfigImport = addImport(path.join(carDir, "body.ini"));
    const tireConfigImport = addImport(path.join(carDir, "tires.ini"));
    const skinFiles = getDirEntries(carDir)
      .filter(
        (file) =>
          file.isFile() &&
          /^skin.*\.png$/i.test(file.name) &&
          !/_damaged\.png$/i.test(file.name),
      )
      .map((file) => file.name)
      .sort((left, right) => left.localeCompare(right, undefined, { numeric: true }));

    const dbCar = dbIndex.get(carId);
    let tireModelImport = null;
    let tireTextureImport = null;

    if (dbCar) {
      const tireNodePath = parseQuotedValue(dbCar.text, "Tires");
      const tireDefinitionPath = tireNodePath
        ? path.join(
            databaseRoot,
            tireNodePath
              .replace(/^root\//, "")
              .replace(/\//g, path.sep)
              .concat(".h"),
          )
        : null;

      if (tireDefinitionPath && fileExists(tireDefinitionPath)) {
        const tireText = fs.readFileSync(tireDefinitionPath, "utf8");
        const tireMeshId = parseNumber(tireText, "MeshId");
        const tireSkinId = parseNumber(tireText, "SkinId");

        if (Number.isFinite(tireMeshId)) {
          const tireModelPath = path.join(
            carsRoot,
            "shared",
            `tire_${Math.round(tireMeshId)}_out.glb`,
          );

          if (fileExists(tireModelPath)) {
            tireModelImport = addImport(tireModelPath);
          }
        }

        if (Number.isFinite(tireSkinId)) {
          const tireTexturePath = path.join(
            carsRoot,
            "shared",
            `tire_${String(Math.round(tireSkinId)).padStart(2, "0")}.png`,
          );

          if (fileExists(tireTexturePath)) {
            tireTextureImport = addImport(tireTexturePath);
          }
        }
      }
    }

    if (!tireModelImport) {
      tireModelImport = addImport(path.join(carsRoot, "shared/tire_4_out.glb"));
    }

    if (!tireTextureImport) {
      tireTextureImport = addImport(path.join(carsRoot, "shared/tire_04.png"));
    }

    const skinsLiteral = skinFiles
      .map((fileName) => {
        const skinImport = addImport(path.join(carDir, fileName));
        const skinId = path.parse(fileName).name;
        return `{
          id: ${JSON.stringify(skinId)},
          label: ${JSON.stringify(skinId)},
          texture: ${skinImport},
        }`;
      })
      .join(",\n");

    const defaultSkinId = skinFiles[0] ? path.parse(skinFiles[0]).name : null;

    return `{
      id: ${JSON.stringify(carId)},
      label: ${JSON.stringify(carId)},
      carModel: ${modelImport},
      tireModel: ${tireModelImport},
      cameraConfig: ${cameraConfigImport},
      bodyConfig: ${bodyConfigImport},
      tireConfig: ${tireConfigImport},
      sharedTextures: {
        common: ${globalImports.sharedTextureImports.common},
        interior: ${globalImports.sharedTextureImports.interior},
        windows: ${globalImports.sharedTextureImports.windows},
        lights: ${globalImports.sharedTextureImports.lights},
        shock: ${globalImports.sharedTextureImports.shock},
        shadow: ${globalImports.sharedTextureImports.shadow},
        tire: ${tireTextureImport},
      },
      skins: [
${indentMultiline(skinsLiteral, 8)}
      ],
      defaultSkinId: ${JSON.stringify(defaultSkinId)},
    }`;
  });

  return carLiterals;
}

function buildTracks(globalImports) {
  const trackFiles = [];
  const pendingDirs = [tracksRoot];

  while (pendingDirs.length > 0) {
    const currentDir = pendingDirs.pop();

    for (const entry of getDirEntries(currentDir)) {
      const fullPath = path.join(currentDir, entry.name);

      if (entry.isDirectory()) {
        pendingDirs.push(fullPath);
        continue;
      }

      if (entry.name === "track_geom_out.glb") {
        trackFiles.push(fullPath);
      }
    }
  }

  trackFiles.sort((left, right) => left.localeCompare(right));

  return trackFiles.map((modelPath) => {
    const relativeParts = path.relative(tracksRoot, modelPath).split(path.sep);
    const [familyId, trackId, variantId] = relativeParts;
    const variantKey = variantId.toLowerCase();
    const trackKey = `${familyId}/${trackId}/${variantId}`;
    const trackDir = path.join(tracksRoot, familyId, trackId, variantId);
    const textureDir = path.join(tracksRoot, familyId, "textures");
    const logPath = path.join(trackDir, "geometry/track_geom_log.txt");
    const logText = fs.readFileSync(logPath, "utf8");
    const textureFileMap = createTextureFileMap(textureDir);
    const requestedTextures = collectRequestedTrackTextures(logText);
    const trackTextureLiterals = requestedTextures
      .map((textureKey) => {
        const texturePath = textureFileMap.get(textureKey);

        if (!texturePath) {
          return null;
        }

        return `${JSON.stringify(textureKey)}: ${addImport(texturePath)}`;
      })
      .filter(Boolean)
      .join(",\n");

    const trackDbInfo = trackDbFolderMap[familyId];

    if (!trackDbInfo) {
      throw new Error(`No database mapping for track family ${familyId}`);
    }

    const weatherBindingPath = path.join(
      databaseRoot,
      "Data/Track",
      trackDbInfo.family,
      trackDbInfo.name,
      variantId.toUpperCase(),
      "Weather[1].h",
    );
    const weatherBindingText = fs.readFileSync(weatherBindingPath, "utf8");
    const weatherNodePath = parseQuotedValue(weatherBindingText, "Settings");
    const weatherName = weatherNodePath?.split("/").pop();
    const weatherPath = path.join(
      databaseRoot,
      weatherNodePath.replace(/^root\//, "").replace(/\//g, path.sep) + ".h",
    );
    const weatherText = fs.readFileSync(weatherPath, "utf8");
    const atmosphereName = parseQuotedValue(weatherText, "Atmosphere")?.split("/").pop() ?? "Grey";
    const atmosphereImport = addImport(path.join(trackDir, "data/atmosphere.ini"));
    const atmospherePresetImport = addImport(
      path.join(globalRoot, "atmosphere", `${atmosphereName.toLowerCase()}.ini`),
    );
    const cloudBottomImport = addImport(
      path.join(globalRoot, "atmosphere", `${atmosphereName.toLowerCase()}_clouds_bottom.png`),
    );
    const cloudTopImport = addImport(
      path.join(globalRoot, "atmosphere", `${atmosphereName.toLowerCase()}_clouds_top.png`),
    );
    const horizonTextureName = normalizeTextureName(
      parseQuotedValue(weatherBindingText, "HorizonTexture"),
    );
    const horizonTexturePath = horizonTextureName
      ? textureFileMap.get(horizonTextureName)
      : null;
    const horizonImport = horizonTexturePath ? addImport(horizonTexturePath) : "null";
    const filterAddImport = addImport(
      path.join(
        globalRoot,
        "filters",
        `${parseQuotedValue(weatherText, "LuminanceFilterAdd")}.tga`,
      ),
    );
    const filterSubImport = addImport(
      path.join(
        globalRoot,
        "filters",
        `${parseQuotedValue(weatherText, "LuminanceFilterSub")}.tga`,
      ),
    );
    const fallbackFlareConfig = addImport(path.join(globalRoot, "flares/day.ini"));
    const fallbackGlowTexture = addImport(path.join(globalRoot, "flares/day_glow.png"));
    const fallbackFlareTexture = addImport(path.join(globalRoot, "flares/day_flares.png"));
    const skyTopImport = resolveSkyTopImport({
      weatherText,
    });

    return `{
      id: ${JSON.stringify(trackKey)},
      label: ${JSON.stringify(`${trackId}/${variantKey}`)},
      familyId: ${JSON.stringify(familyId)},
      trackId: ${JSON.stringify(trackId)},
      variantId: ${JSON.stringify(variantKey)},
      model: ${addImport(modelPath)},
      log: ${addImport(logPath)},
      startPoints: ${addImport(path.join(trackDir, "data/startpoints.bed"))},
      lightmap: ${addImport(path.join(trackDir, "lighting/lightmap1_w2.png"))},
      trackTextures: {
${indentMultiline(trackTextureLiterals, 8)}
      },
      environment: {
        atmosphere: ${atmosphereImport},
        atmospherePreset: ${atmospherePresetImport},
        horizonTexture: ${horizonImport},
        skyTopTexture: ${skyTopImport},
        filterAddTexture: ${filterAddImport},
        filterSubTexture: ${filterSubImport},
        cloudBottomTexture: ${cloudBottomImport},
        cloudTopTexture: ${cloudTopImport},
        flareConfig: ${fallbackFlareConfig},
        glowTexture: ${fallbackGlowTexture},
        flareTexture: ${fallbackFlareTexture},
        flareConfigByName: {
${indentMultiline(
  Object.entries(globalImports.flareConfigByName)
    .map(([key, value]) => `${JSON.stringify(key)}: ${value}`)
    .join(",\n"),
  10,
)}
        },
        flareGlowTextureByName: {
${indentMultiline(
  Object.entries(globalImports.flareGlowTextureByName)
    .map(([key, value]) => `${JSON.stringify(key)}: ${value}`)
    .join(",\n"),
  10,
)}
        },
        flareTextureByName: {
${indentMultiline(
  Object.entries(globalImports.flareTextureByName)
    .map(([key, value]) => `${JSON.stringify(key)}: ${value}`)
    .join(",\n"),
  10,
)}
        },
        weatherProfile: ${buildWeatherProfileLiteral(weatherText, weatherName)},
      },
    }`;
  });
}

function indentMultiline(text, spaces) {
  if (!text) {
    return "";
  }

  const prefix = " ".repeat(spaces);
  return text
    .split("\n")
    .map((line) => `${prefix}${line}`)
    .join("\n");
}

function writeCatalog() {
  const globalImports = createGlobalImports();
  const carLiterals = buildCars(globalImports);
  const trackLiterals = buildTracks(globalImports);

  lines.push("");
  lines.push("export const carCatalog = [");
  lines.push(indentMultiline(carLiterals.join(",\n"), 2));
  lines.push("];");
  lines.push("");
  lines.push("export const trackCatalog = [");
  lines.push(indentMultiline(trackLiterals.join(",\n"), 2));
  lines.push("];");
  lines.push("");
  lines.push("export const defaultSelection = {");
  lines.push("  trackId: trackCatalog[0]?.id ?? null,");
  lines.push("  carId: carCatalog[0]?.id ?? null,");
  lines.push("  skinId: carCatalog[0]?.defaultSkinId ?? null,");
  lines.push("};");
  lines.push("");

  fs.mkdirSync(path.dirname(outputPath), { recursive: true });
  fs.writeFileSync(outputPath, `${lines.join("\n")}\n`);
  console.log(`Wrote runtime catalog to ${outputPath}`);
}

writeCatalog();
