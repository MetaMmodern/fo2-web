const fs = require("fs");
const path = require("path");

const repoRoot = path.resolve(__dirname, "..");
const logPath = path.join(
  repoRoot,
  "src/data/tracks/arena/arena1/a/geometry/track_geom_log.txt",
);
const texturesDir = path.join(repoRoot, "src/data/tracks/arena/textures");
const outputPath = path.join(
  repoRoot,
  "src/game/generated/arenaTrackTextureManifest.js",
);

const aliases = {
  colormap: "col",
};

const logText = fs.readFileSync(logPath, "utf8");
const textureFiles = fs
  .readdirSync(texturesDir)
  .filter((fileName) => fileName.toLowerCase().endsWith(".png"))
  .sort((a, b) => a.localeCompare(b));

const fileMap = new Map(
  textureFiles.map((fileName) => [path.parse(fileName).name.toLowerCase(), fileName]),
);

const requestedTextureNames = Array.from(
  new Set(
    logText
      .split(/\r?\n/)
      .filter((line) => /^Texture [123]:/.test(line))
      .map((line) => line.replace(/^Texture [123]:\s*/, "").trim())
      .filter(Boolean),
  ),
);

const resolvedEntries = requestedTextureNames
  .map((textureName) => {
    const baseName = path.parse(textureName).name.toLowerCase();
    const resolvedBaseName = aliases[baseName] || baseName;
    const matchedFileName = fileMap.get(resolvedBaseName);

    if (!matchedFileName) {
      throw new Error(`No PNG file found for ${textureName}`);
    }

    return {
      key: resolvedBaseName,
      fileName: matchedFileName,
    };
  })
  .sort((a, b) => a.key.localeCompare(b.key));

const lines = [
  "// Generated by tools/generate-track-texture-manifest.js",
  "",
];

for (const [index, entry] of resolvedEntries.entries()) {
  lines.push(
    `import texture${index}Url from "url:../../data/tracks/arena/textures/${entry.fileName}";`,
  );
}

lines.push("", "export const arenaTrackTextureUrls = {");

for (const [index, entry] of resolvedEntries.entries()) {
  lines.push(`  ${JSON.stringify(entry.key)}: texture${index}Url,`);
}

lines.push("};", "");

fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, `${lines.join("\n")}`);

console.log(`Wrote ${resolvedEntries.length} texture URLs to ${outputPath}`);
